package Loops;

public class Forloop {
	
	public static void main (String [] args) {
		
		for (int i =1; i<=3; i++) {
			System.out.println("a");
			System.out.println("b");
		}
		//Numbers
		System.out.println("Numbers");
		for (int i=1; i<=5; i++) {
			System.out.println(i);
		}
		//Even
		System.out.println("Even Number");
		for (int i =0; i<=10; i=i+2) {
			System.out.println(i);
		}
	}

}
