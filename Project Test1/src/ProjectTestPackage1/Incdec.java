package ProjectTestPackage1;

public class Incdec {
	
	public static void main (String [] args) {
		
		int a = 10;
		
		System.out.println(a);
		System.out.println(a++);
		System.out.println(a);
		System.out.println(++a);
		
		int b = 20;
		System.out.println(b);
		System.out.println(b--);
		System.out.println(b);
		System.out.println(--b);
		System.out.println(b);
		System.out.println(--b);
		System.out.println(--b);
		System.out.println(b--);
		System.out.println(b);
	}

}
