package ProjectTestPackage1;

public class RelationalOperator {
	
	public static void main (String [] args) {
		
		int a = 5 , b = 10;
		
		System.out.println(a);
		System.out.println(a<b); // less than 
		System.out.println(a>b); // greater than
		System.out.println(a<=b); // less then equals 
		System.out.println(a>=b); // greater than equals
		
		int c = 4 , d = 4;
		
		System.out.println(c==d); // Equals to
		System.out.println(c!=d); // Not equals to
	}

}
